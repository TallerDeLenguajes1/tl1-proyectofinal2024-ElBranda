            if (move > 80) {
                if (rn==0) {
                    enemies[i].MoveUp();
                } else if (rn==1) {
                    enemies[i].MoveDwn();
                } else if (rn==2) {
                    enemies[i].MoveRgt();
                } else {
                    enemies[i].MoveLft();
                }
            }

            if (shothit > 85) {
                enemies[i].isShooting=true;
            }

            if (gunmove > 85) {
                if (rn2==0) {
                    enemies[i].GunUp();
                } else if (rn2==1) {
                    enemies[i].GunDwn();
                } else if (rn2==2) {
                    enemies[i].GunRgt();
                } else {
                    enemies[i].GunLft();
                }
            }

            if (enemies[i].isShooting) {
                enemies[i].Shot();
            }
        } else {
            totalDeath++;
        }
