                playerT.getY <= enemies[i].getBulletY && playerT.getY+2 >= enemies[i].getBulletY) {
                playerT.Hit();
            }

            if (move > 80) {
                if (rn==0) {
                    enemies[i].MoveUp();
                } else if (rn==1) {
                    enemies[i].MoveDwn();
                } else if (rn==2) {
                    enemies[i].MoveRgt();
                } else {
                    enemies[i].MoveLft();
                }
            }

            if (shothit > 85) {
                enemies[i].isShooting=true;
            }

            if (gunmove > 85) {
                if (rn2==0) {
                    enemies[i].GunUp();
                } else if (rn2==1) {
                    enemies[i].GunDwn();
                } else if (rn2==2) {
                    enemies[i].GunRgt();
                } else {
                    enemies[i].GunLft();
                }
            }

            if (enemies[i].isShooting) {
                enemies[i].Shot();
            }
        }
    }

    if (Console.KeyAvailable) {
        ConsoleKeyInfo keyInfo = Console.ReadKey(intercept: true);

        switch (keyInfo.Key) {
            case ConsoleKey.W: playerT.GunUp(); break;
            case ConsoleKey.A: playerT.GunLft(); break;
            case ConsoleKey.S: playerT.GunDwn(); break;
            case ConsoleKey.D: playerT.GunRgt(); break;
            case ConsoleKey.UpArrow: {
                playerT.MoveUp();
                break;
            }
            case ConsoleKey.LeftArrow: {
                playerT.MoveLft();
                break;
            }
            case ConsoleKey.DownArrow: {
                playerT.MoveDwn();
                break;
            }
            case ConsoleKey.RightArrow: {
                playerT.MoveRgt();
                break;
            }
            case ConsoleKey.Spacebar: playerT.isShooting = true; break;
        }
    }

    //Thread.Sleep(1);
}