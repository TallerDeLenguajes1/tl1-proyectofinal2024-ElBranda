Tank playerT = new Tank(10,10);
playerT.SetLimits(stage, 5,5);
playerT.setLife = 500;

Tank[] enemies = [
    new Tank(20,25),
    new Tank(10,10)
];

for (int i = 0; i < enemies.Length; i++) {
    enemies[i].SetLimits(stage,5,5);
}

Camera camera = new Camera(stage, playerT, enemies, 5, 5);

while (true) {
    Console.CursorVisible = false;
    camera.Update(stage, playerT, enemies, 5, 5);
    camera.Show();
    playerT.Print();

    if (playerT.isShooting) {
        playerT.Shot();
    }

    for (int i = 0; i < enemies.Length; i++) {
        if (!enemies[i].dead) {
            int rn = new Random().Next(0,5);
            int move = new Random().Next(0,101);
            int shothit = new Random().Next(0,101);
            int gunmove = new Random().Next(0,101);
            int rn2 = new Random().Next(0,5);

            if (enemies[i].getX <= playerT.getBulletX && enemies[i].getX+4 >= playerT.getBulletX &&
                enemies[i].getY <= playerT.getBulletY && enemies[i].getY+2 >= playerT.getBulletY) {
                enemies[i].Hit();
            }

            if (playerT.getX <= enemies[i].getBulletX && playerT.getX+4 >= enemies[i].getBulletX &&
                playerT.getY <= enemies[i].getBulletY && playerT.getY+2 >= enemies[i].getBulletY) {
                playerT.Hit();
            }

            if (move > 80) {
                if (rn==0) {
                    enemies[i].MoveUp();
                } else if (rn==1) {
                    enemies[i].MoveDwn();
                } else if (rn==2) {
                    enemies[i].MoveRgt();
                } else {
                    enemies[i].MoveLft();
                }
            }

            if (shothit > 85) {
                enemies[i].isShooting=true;
            }

            if (gunmove > 85) {
                if (rn2==0) {
                    enemies[i].GunUp();
                } else if (rn2==1) {
                    enemies[i].GunDwn();
                } else if (rn2==2) {
                    enemies[i].GunRgt();
                } else {
                    enemies[i].GunLft();
                }
            }

            if (enemies[i].isShooting) {
                enemies[i].Shot();
            }
        }
    }

    if (Console.KeyAvailable) {
        ConsoleKeyInfo keyInfo = Console.ReadKey(intercept: true);

        switch (keyInfo.Key) {
            case ConsoleKey.W: playerT.GunUp(); break;
            case ConsoleKey.A: playerT.GunLft(); break;
            case ConsoleKey.S: playerT.GunDwn(); break;
            case ConsoleKey.D: playerT.GunRgt(); break;
            case ConsoleKey.UpArrow: {
                playerT.MoveUp();
                break;
            }
            case ConsoleKey.LeftArrow: {
                playerT.MoveLft();
                break;
            }
            case ConsoleKey.DownArrow: {
                playerT.MoveDwn();
                break;
            }
            case ConsoleKey.RightArrow: {
                playerT.MoveRgt();
                break;
            }
            case ConsoleKey.Spacebar: playerT.isShooting = true; break;
        }
    }

    //Thread.Sleep(1);
}